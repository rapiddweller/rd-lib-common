package com.rapiddweller;

/**
 * Simple JavaBean used for testing.
 * Created: 19.08.2007 15:27:37
 */
public final class SomeBean {

    public int num;
    public String text;

    public SomeBean() {
        this(0, null);
    }

    public SomeBean(int num, String text) {
        this.num = num;
        this.text = text;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
